public class TwoDarray1 {
    public static void main(String[] args) {
        int arr [][] = {
            {11,12,14},
            {16,17,18},
            {19,20,21}
        };
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                System.out.print(arr[i] [j] + " ");
            }
            System.out.println("");
        }
    }
}
